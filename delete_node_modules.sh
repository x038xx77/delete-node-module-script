#!/bin/bash

# Папка, в которой искать node_modules (по умолчанию - /home/bb/DevBB)
SEARCH_DIR=${1:-/home/bb/DevBB}

# Ищем все папки node_modules в указанной директории и удаляем их
find "$SEARCH_DIR" -type d -name "node_modules" -prune -exec rm -rf {} +

echo "Все папки node_modules в $SEARCH_DIR удалены."
